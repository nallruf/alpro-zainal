public class Mahasiswa {
    private long nim;
    private String nama;

    public void setNim(long value){
        this.nim = value;
    } 

    public long getNim(){
        return this.nim;
    }

    public void setNama(String value){
        this.nama = value;
    }

    public String getNama(){
        return this.nama;
    }
}
