public class Mahasiswa {
    private String nim;
    private String nama;
    
    public void setNim(String value){
        this.nim = value;
    }

    public String getNim(){
        return this.nim;
    }

    public void setNama(String value){
        this.nama = value;
    }

    public String getNama(){
        return this.nama;
    }
}
